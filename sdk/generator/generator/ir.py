import collections.abc
import enum
import re
import typing

import openapi_pydantic as op
from pydantic import BaseModel, Discriminator

from generator.casing import to_pascal_case


class PrimitiveType(BaseModel):
    """A scalar value type: string, integer, number, boolean, or unknown."""

    kind: typing.Literal["primitive"]
    type: typing.Literal["string", "integer", "number", "boolean", "unknown"]
    format: str | None = None


class ArrayType(BaseModel):
    """An ordered sequence of items all sharing the same type."""

    kind: typing.Literal["array"]
    items: "TypeRef"


class ModelRef(BaseModel):
    """A by-name reference to a Model entry in the top-level models list."""

    kind: typing.Literal["model"]
    name: str


class EnumRef(BaseModel):
    """A by-name reference to an Enum entry in the top-level enums list."""

    kind: typing.Literal["enum"]
    name: str


class UnionRef(BaseModel):
    """A by-name reference to a NamedUnion entry in the top-level unions list."""

    kind: typing.Literal["union_ref"]
    name: str


class UnionDiscriminator(BaseModel):
    """
    Discriminator metadata for a tagged union.

    Derived from the OpenAPI discriminator object. The mapping values are
    schema names (not full $ref paths).
    """

    property_name: str
    mapping: dict[str, str]


class UnionType(BaseModel):
    """
    A type that can be any one of several variants.

    Produced by anyOf, oneOf, or allOf. The composition_kind preserves which
    keyword was used so generators can distinguish inheritance (allOf) from
    exclusive union (oneOf) from open union (anyOf).
    """

    kind: typing.Literal["union"]
    variants: list["TypeRef"]
    discriminator: UnionDiscriminator | None = None
    composition_kind: typing.Literal["allOf", "oneOf", "anyOf"] | None = None


class NullableType(BaseModel):
    """
    A wrapper marking a type as nullable.

    Produced by the anyOf: [{...}, {type: null}] pattern that is dominant in
    OpenAPI 3.1 specs generated by FastAPI / Pydantic.
    """

    kind: typing.Literal["nullable"]
    inner: "TypeRef"


class LiteralType(BaseModel):
    """
    A single fixed value.

    Produced by const schemas and anonymous inline enums (enums without a
    title, which cannot be promoted to a named Enum entry).
    """

    kind: typing.Literal["literal"]
    value: str | int | float | bool | None


class MapType(BaseModel):
    """
    A dictionary with typed values.

    Produced by schemas that use additionalProperties without declaring explicit
    properties. key_type defaults to string when absent.
    """

    kind: typing.Literal["map"]
    value_type: "TypeRef"
    key_type: "TypeRef | None" = None


type TypeRef = typing.Annotated[
    PrimitiveType
    | ArrayType
    | ModelRef
    | EnumRef
    | UnionRef
    | UnionType
    | NullableType
    | LiteralType
    | MapType,
    Discriminator("kind"),
]


class Field(BaseModel):
    """A single property of a Model, with optional read/write and deprecation metadata."""

    name: str
    type: TypeRef
    required: bool
    description: str | None = None
    read_only: bool | None = None
    write_only: bool | None = None
    deprecated: bool | None = None
    default: typing.Any | None = None


class Model(BaseModel):
    """
    A named object schema from components/schemas.

    Emitted as a struct, dataclass, or interface by language generators.
    Models with no fields represent union or composition schemas (oneOf/anyOf
    at the top level) whose variants are discovered transitively.
    """

    name: str
    description: str | None = None
    fields: list[Field]


class EnumValue(BaseModel):
    """A single member of an Enum with its UPPER_SNAKE_CASE display name and raw value."""

    name: str
    value: str | int | float | bool


class Enum(BaseModel):
    """
    A named enum schema from components/schemas.

    Emitted as a string or integer enum by language generators.
    """

    name: str
    type: str
    description: str | None = None
    values: list[EnumValue]


class NamedUnion(BaseModel):
    """
    A named union schema from components/schemas.

    Produced by oneOf, anyOf, or allOf at the top level when the schema has no
    properties of its own. Referenced by UnionRef in TypeRef trees. Generators
    emit this as a discriminated union type or type alias.
    """

    name: str
    description: str | None = None
    variants: list[TypeRef]
    discriminator: UnionDiscriminator | None = None
    composition_kind: typing.Literal["allOf", "oneOf", "anyOf"] | None = None


class ErrorResponse(BaseModel):
    """A non-2xx HTTP response returned by an operation, with its status code and optional body type."""

    name: str
    status_code: int
    response_type: ResponseType
    type: TypeRef | None = None
    description: str | None = None


class Parameter(BaseModel):
    """A path or query parameter on a Method."""

    name: str
    parameter_name: str
    type: TypeRef
    required: bool
    description: str | None = None
    deprecated: bool | None = None
    default: typing.Any | None = None


class HTTPMethod(enum.StrEnum):
    """Standard HTTP verbs as uppercase strings."""

    GET = "GET"
    POST = "POST"
    PUT = "PUT"
    DELETE = "DELETE"
    PATCH = "PATCH"
    HEAD = "HEAD"
    OPTIONS = "OPTIONS"
    TRACE = "TRACE"


type ResponseType = typing.Literal["json", "text", "none"]


PAGINATION = "x-polar-pagination"
type PaginationType = typing.Literal["page_limit"]


class Pagination(BaseModel):
    """Pagination metadata for a Method that returns a paginated list of items."""

    type: PaginationType
    item_schema: TypeRef


class Method(BaseModel):
    """A single API endpoint grouped under a Service."""

    name: str
    description: str | None = None
    http_method: HTTPMethod
    path: str
    path_params: list[Parameter]
    query_params: list[Parameter]
    response_type: ResponseType
    body: TypeRef | None = None
    response: TypeRef | None = None
    errors: list[ErrorResponse] = []
    deprecated: bool | None = None
    pagination: Pagination | None = None


class Service(BaseModel):
    """
    A named group of related Methods.

    Services may be nested to reflect operationId hierarchies
    (e.g. products:variants:list → products > variants > list).
    """

    name: str
    services: list["Service"]
    methods: list["Method"]


class APIVersion(BaseModel):
    """
    A single version of an API, with its services and models.

    Contains all services and named schemas extracted from the spec. Only
    schemas actually referenced by operations are included.

    Schemas are split by usage context:
    - input_*  : referenced from request bodies or parameters (client → server).
    - output_* : referenced from success/error responses (server → client).
    A schema referenced in both contexts appears in both lists.
    Enums are shared across contexts and appear in a single list.
    """

    version: str
    services: list[Service]
    input_models: list[Model]
    output_models: list[Model]
    enums: list[Enum]
    input_unions: list[NamedUnion]
    output_unions: list[NamedUnion]


class APIIR(BaseModel):
    """
    The intermediate representation of our API specifications, generated from OpenAPI specs.

    This IR is used to generate code for the SDK.
    """

    versions: list[APIVersion]


type IsPrivateOperationFilter = typing.Callable[[op.Operation], bool]


def _default_is_private_operation(operation: op.Operation) -> bool:
    return "private" in (operation.tags or [])


type ServiceKeyGetter = typing.Callable[[op.Operation], list[str]]


def _default_get_service_key(operation: op.Operation) -> list[str]:
    if not operation.operationId:
        raise ValueError(f"Operation {operation.operationId} has no operation ID.")
    return operation.operationId.split(":")[:-1]


type MethodNameGetter = typing.Callable[[op.Operation], str]


def _default_get_method_name(operation: op.Operation) -> str:
    if not operation.operationId:
        raise ValueError(f"Operation {operation.operationId} has no operation ID.")
    return operation.operationId.split(":")[-1]


type ModelNameNormalizer = typing.Callable[[str], str]


def _default_model_name_normalizer(input: str) -> str:
    parts = [p for p in re.split(r"[\W_]+", input) if p]
    name = "".join(f"{first.upper()}{''.join(rest)}" for first, *rest in parts)
    if name[0].isdigit():
        name = "_" + name
    return name


type ServiceNameNormalizer = typing.Callable[[str], str]


def _default_service_name_normalizer(input: str) -> str:
    parts = [p for p in re.split(r"[\W_]+", input) if p]
    name = "".join(f"{first.upper()}{''.join(rest)}" for first, *rest in parts)
    if name[0].isdigit():
        name = "_" + name
    return name


def _get_operations(
    path_item: op.PathItem,
) -> collections.abc.Generator[tuple[HTTPMethod, op.Operation], None, None]:
    for method in HTTPMethod:
        if operation := getattr(path_item, method.lower(), None):
            yield method, operation


def _resolve_reference[T](ref: op.Reference | T, spec: op.OpenAPI) -> T:
    if not isinstance(ref, op.Reference):
        return ref

    ref_path = ref.ref.lstrip("#/").split("/")
    current: typing.Any = spec
    for part in ref_path:
        if isinstance(current, dict):
            try:
                current = current[part]
            except KeyError as e:
                raise ValueError(f"Reference {ref.ref} is invalid.") from e
        else:
            try:
                current = getattr(current, part)
            except AttributeError as e:
                raise ValueError(f"Reference {ref.ref} is invalid.") from e

    return current


def _extract_discriminator(
    discriminator: op.Discriminator | None,
) -> UnionDiscriminator | None:
    if discriminator is None:
        return None
    mapping = {}
    if discriminator.mapping:
        for key, ref_path in discriminator.mapping.items():
            mapping[key] = ref_path.split("/")[-1]
    return UnionDiscriminator(property_name=discriminator.propertyName, mapping=mapping)


def _is_null_schema(schema_or_ref: op.Schema | op.Reference) -> bool:
    return isinstance(schema_or_ref, op.Schema) and schema_or_ref.type == "null"


def _is_union_schema(schema: op.Schema) -> bool:
    """Return True when the schema is a pure composition/union with no properties of its own."""
    has_composition = (
        schema.anyOf is not None or schema.oneOf is not None or schema.allOf is not None
    )
    has_own_properties = schema.properties is not None or schema.type == "object"
    return has_composition and not has_own_properties and schema.enum is None


def _convert_typeref(
    schema_or_ref: op.Schema | op.Reference,
    component_schemas: dict[str, op.Schema],
    inline_schemas: dict[str, op.Schema],
    referenced_names: set[str],
    *context_sets: set[str],
    normalize_model_name: ModelNameNormalizer,
) -> TypeRef:
    """Convert an OpenAPI schema or $ref to the IR TypeRef type."""
    if isinstance(schema_or_ref, op.Reference):
        original_name = schema_or_ref.ref.split("/")[-1]
        name = normalize_model_name(original_name)
        referenced_names.add(name)
        for cs in context_sets:
            cs.add(name)
        target = component_schemas.get(original_name)
        if target is not None and target.enum is not None:
            return EnumRef(kind="enum", name=name)
        if target is not None and _is_union_schema(target):
            return UnionRef(kind="union_ref", name=name)
        return ModelRef(kind="model", name=name)

    schema = schema_or_ref

    if schema.const is not None:
        return LiteralType(kind="literal", value=schema.const)

    if schema.anyOf:
        null_variants = [v for v in schema.anyOf if _is_null_schema(v)]
        non_null = [v for v in schema.anyOf if not _is_null_schema(v)]
        if null_variants and len(non_null) == 1:
            inner = _convert_typeref(
                non_null[0],
                component_schemas,
                inline_schemas,
                referenced_names,
                *context_sets,
                normalize_model_name=normalize_model_name,
            )
            return NullableType(kind="nullable", inner=inner)
        variants = [
            _convert_typeref(
                v,
                component_schemas,
                inline_schemas,
                referenced_names,
                *context_sets,
                normalize_model_name=normalize_model_name,
            )
            for v in schema.anyOf
        ]
        discriminator = _extract_discriminator(schema.discriminator)
        return UnionType(
            kind="union",
            variants=variants,
            discriminator=discriminator,
            composition_kind="anyOf",
        )

    if schema.oneOf:
        variants = [
            _convert_typeref(
                v,
                component_schemas,
                inline_schemas,
                referenced_names,
                *context_sets,
                normalize_model_name=normalize_model_name,
            )
            for v in schema.oneOf
        ]
        discriminator = _extract_discriminator(schema.discriminator)
        return UnionType(
            kind="union",
            variants=variants,
            discriminator=discriminator,
            composition_kind="oneOf",
        )

    if schema.allOf:
        variants = [
            _convert_typeref(
                v,
                component_schemas,
                inline_schemas,
                referenced_names,
                *context_sets,
                normalize_model_name=normalize_model_name,
            )
            for v in schema.allOf
        ]
        return UnionType(kind="union", variants=variants, composition_kind="allOf")

    if schema.enum is not None:
        if schema.title:
            name = normalize_model_name(schema.title)
            if name not in inline_schemas:
                inline_schemas[name] = schema
            referenced_names.add(name)
            for cs in context_sets:
                cs.add(name)
            return EnumRef(kind="enum", name=name)
        literals: list[TypeRef] = [
            LiteralType(kind="literal", value=v) for v in schema.enum
        ]
        if len(literals) == 1:
            return literals[0]
        return UnionType(kind="union", variants=literals)

    schema_type = schema.type if isinstance(schema.type, str) else None

    if schema_type in ("string", "integer", "number", "boolean"):
        return PrimitiveType(
            kind="primitive",
            type=schema_type,  # type: ignore[arg-type]
            format=schema.schema_format or None,
        )

    if schema_type == "null":
        return LiteralType(kind="literal", value=None)

    if schema_type == "array":
        if schema.items is not None and not isinstance(schema.items, list):
            items = _convert_typeref(
                schema.items,
                component_schemas,
                inline_schemas,
                referenced_names,
                *context_sets,
                normalize_model_name=normalize_model_name,
            )
        else:
            items: TypeRef = PrimitiveType(kind="primitive", type="unknown")
        return ArrayType(kind="array", items=items)

    if schema_type == "object" or schema.properties is not None:
        if schema.properties is None and schema.additionalProperties is not None:
            if isinstance(schema.additionalProperties, bool):
                if not schema.additionalProperties:
                    return PrimitiveType(kind="primitive", type="unknown")
                return MapType(
                    kind="map",
                    value_type=PrimitiveType(kind="primitive", type="unknown"),
                )
            value_type = _convert_typeref(
                schema.additionalProperties,
                component_schemas,
                inline_schemas,
                referenced_names,
                *context_sets,
                normalize_model_name=normalize_model_name,
            )
            return MapType(kind="map", value_type=value_type)
        if schema.title:
            name = normalize_model_name(schema.title)
            if name not in inline_schemas:
                inline_schemas[name] = schema
            referenced_names.add(name)
            for cs in context_sets:
                cs.add(name)
            return ModelRef(kind="model", name=name)
        return PrimitiveType(kind="primitive", type="unknown")

    return PrimitiveType(kind="primitive", type="unknown")


def _schema_to_enum(name: str, schema: op.Schema) -> Enum:
    enum_type = schema.type.value if isinstance(schema.type, op.DataType) else "unknown"
    values = [
        EnumValue(
            name=re.sub(r"\W+", "_", str(v)).upper(),
            value=v,
        )
        for v in (schema.enum or [])
    ]
    return Enum(
        name=name,
        type=enum_type,
        description=schema.description or None,
        values=values,
    )


def _schema_to_model(
    name: str,
    schema: op.Schema,
    component_schemas: dict[str, op.Schema],
    inline_schemas: dict[str, op.Schema],
    referenced_names: set[str],
    *context_sets: set[str],
    normalize_model_name: ModelNameNormalizer,
) -> Model:
    required_set = set(schema.required or [])
    fields = []
    for prop_name, prop in (schema.properties or {}).items():
        field_type = _convert_typeref(
            prop,
            component_schemas,
            inline_schemas,
            referenced_names,
            *context_sets,
            normalize_model_name=normalize_model_name,
        )
        description = None
        read_only = None
        write_only = None
        deprecated = None
        default = None
        if isinstance(prop, op.Schema):
            description = prop.description or None
            read_only = True if prop.readOnly else None
            write_only = True if prop.writeOnly else None
            deprecated = True if prop.deprecated else None
            # Extract default value from schema
            if hasattr(prop, "default") and prop.default is not None:
                default = prop.default
        fields.append(
            Field(
                name=prop_name,
                type=field_type,
                required=prop_name in required_set,
                description=description,
                read_only=read_only,
                write_only=write_only,
                deprecated=deprecated,
                default=default,
            )
        )
    # Walk composition keywords so transitive references are discovered even
    # when a model has no explicit properties (e.g. a discriminated oneOf).
    for variant in (schema.anyOf or []) + (schema.oneOf or []) + (schema.allOf or []):
        _convert_typeref(
            variant,
            component_schemas,
            inline_schemas,
            referenced_names,
            *context_sets,
            normalize_model_name=normalize_model_name,
        )
    return Model(name=name, description=schema.description or None, fields=fields)


def _schema_to_named_union(
    name: str,
    schema: op.Schema,
    component_schemas: dict[str, op.Schema],
    inline_schemas: dict[str, op.Schema],
    referenced_names: set[str],
    *context_sets: set[str],
    normalize_model_name: ModelNameNormalizer,
) -> NamedUnion:
    if schema.anyOf:
        composition_kind: typing.Literal["allOf", "oneOf", "anyOf"] = "anyOf"
        variants_raw = schema.anyOf
    elif schema.oneOf:
        composition_kind = "oneOf"
        variants_raw = schema.oneOf
    else:
        composition_kind = "allOf"
        variants_raw = schema.allOf or []
    variants = [
        _convert_typeref(
            v,
            component_schemas,
            inline_schemas,
            referenced_names,
            *context_sets,
            normalize_model_name=normalize_model_name,
        )
        for v in variants_raw
    ]
    return NamedUnion(
        name=name,
        description=schema.description or None,
        variants=variants,
        discriminator=_extract_discriminator(schema.discriminator),
        composition_kind=composition_kind,
    )


def _get_body_schema_raw(
    operation: op.Operation, spec: op.OpenAPI
) -> op.Schema | op.Reference | None:
    if operation.requestBody is None:
        return None

    request_body = _resolve_reference(operation.requestBody, spec)

    try:
        media_type = request_body.content["application/json"]
    except KeyError:
        return None

    return media_type.media_type_schema


def _get_response_type_schema(
    content: dict[str, op.MediaType] | None,
) -> tuple[ResponseType, op.Schema | op.Reference | None]:
    if content is None:
        return "none", None

    for content_type, media_type_obj in content.items():
        if content_type == "application/json":
            return "json", media_type_obj.media_type_schema
        elif content_type.startswith("text/"):
            return "text", media_type_obj.media_type_schema

    return "none", None


def _get_success_response_schema_raw(
    operation: op.Operation, spec: op.OpenAPI
) -> tuple[ResponseType, op.Schema | op.Reference | None]:
    """Return (schema, response_type) for the first matching 2xx response."""
    if operation.responses is None:
        return "none", None

    for status_code in sorted(operation.responses.keys()):
        if not status_code.startswith("2"):
            continue

        response = _resolve_reference(operation.responses[status_code], spec)

        if response.content is None:
            continue

        return _get_response_type_schema(response.content)

    return "none", None


def _get_error_responses_raw(
    operation: op.Operation, spec: op.OpenAPI
) -> list[tuple[int, ResponseType, op.Schema | op.Reference | None, str | None]]:
    """Return (status_code, schema_or_ref, response_type, description) for all non-2xx responses."""
    if operation.responses is None:
        return []

    results: list[
        tuple[int, ResponseType, op.Schema | op.Reference | None, str | None]
    ] = []
    for status_code_str, response_or_ref in operation.responses.items():
        if status_code_str.startswith("2"):
            continue
        try:
            status_code = int(status_code_str)
        except ValueError:
            continue  # Skip "default", "4XX" wildcards, etc.

        response = _resolve_reference(response_or_ref, spec)

        response_type, schema = _get_response_type_schema(response.content)

        results.append((status_code, response_type, schema, response.description))

    return sorted(results, key=lambda x: x[0])


def _get_schema_field_names(
    schema_or_ref: op.Schema | op.Reference | None,
    component_schemas: dict[str, op.Schema],
    inline_schemas: dict[str, op.Schema],
) -> set[str]:
    """Extract field names from a schema or reference."""
    if schema_or_ref is None:
        return set()

    # Resolve the reference if it's a Reference object
    if isinstance(schema_or_ref, op.Reference):
        ref_name = schema_or_ref.ref.split("/")[-1]
        # Try to find in component_schemas first, then inline_schemas
        schema = component_schemas.get(ref_name) or inline_schemas.get(ref_name)
        if schema is None:
            return set()
    else:
        schema = schema_or_ref

    if not isinstance(schema, op.Schema):
        return set()

    if schema.properties is None:
        return set()

    return set(schema.properties.keys())


def _convert_parameter_ir(
    parameter: op.Parameter,
    component_schemas: dict[str, op.Schema],
    inline_schemas: dict[str, op.Schema],
    referenced_names: set[str],
    *context_sets: set[str],
    normalize_model_name: ModelNameNormalizer,
    body_schema: op.Schema | op.Reference | None = None,
) -> Parameter:
    if parameter.param_schema is not None:
        type_ = _convert_typeref(
            parameter.param_schema,
            component_schemas,
            inline_schemas,
            referenced_names,
            *context_sets,
            normalize_model_name=normalize_model_name,
        )
    else:
        type_ = PrimitiveType(kind="primitive", type="unknown")

    # Extract default value from parameter schema
    default = None
    if parameter.param_schema is not None and isinstance(
        parameter.param_schema, op.Schema
    ):
        # OpenAPI default values can be in param_schema.default
        # For OpenAPI 3.1, the default is a field on the schema
        if (
            hasattr(parameter.param_schema, "default")
            and parameter.param_schema.default is not None
        ):
            default = parameter.param_schema.default

    # Determine the parameter_name
    # If the parameter name conflicts with a field in the body, disambiguate it
    parameter_name = parameter.name
    if body_schema is not None:
        body_field_names = _get_schema_field_names(
            body_schema, component_schemas, inline_schemas
        )
        if parameter.name in body_field_names:
            # Disambiguate by appending the parameter location
            if parameter.param_in == op.ParameterLocation.PATH:
                parameter_name = f"{parameter.name}_path"
            elif parameter.param_in == op.ParameterLocation.QUERY:
                parameter_name = f"{parameter.name}_query"

    return Parameter(
        name=parameter.name,
        parameter_name=parameter_name,
        type=type_,
        required=parameter.required or False,
        description=parameter.description or None,
        deprecated=True if parameter.deprecated else None,
        default=default,
    )


def _topological_sort_unions(
    unions: dict[str, NamedUnion],
    names: set[str],
) -> list[NamedUnion]:
    """
    Sort unions in topological order so that child unions come before parent unions.

    A union A depends on union B if A has a UnionRef variant pointing to B.
    We need B to be defined before A in the output.
    """
    # Filter to only the unions we care about
    filtered_unions = {name: union for name, union in unions.items() if name in names}

    # Build dependency graph: for each union, find which other unions it depends on
    # dependencies[union_name] = set of union names that must come before it
    dependencies: dict[str, set[str]] = {name: set() for name in filtered_unions}

    for union_name, union in filtered_unions.items():
        for variant in union.variants:
            if isinstance(variant, UnionRef):
                # This union depends on the referenced union
                # But only if the referenced union is also in our filtered set
                if variant.name in filtered_unions:
                    dependencies[union_name].add(variant.name)

    # Perform topological sort using Kahn's algorithm
    result: list[NamedUnion] = []
    in_degree: dict[str, int] = {name: 0 for name in filtered_unions}

    # Count incoming edges
    # If A depends on B (dependencies[A] contains B), then B must come before A
    # This means there's an edge B -> A, so in_degree[A] += 1
    for union_name, deps in dependencies.items():
        for dep in deps:
            in_degree[union_name] += 1

    # Start with unions that have no dependencies
    queue = [name for name in filtered_unions if in_degree[name] == 0]
    # Sort alphabetically for deterministic output
    queue.sort()

    while queue:
        # Take the next union with no dependencies
        name = queue.pop(0)
        result.append(filtered_unions[name])

        # For each union that depends on this one, reduce its in-degree
        for union_name, deps in dependencies.items():
            if name in deps:
                in_degree[union_name] -= 1
                if in_degree[union_name] == 0:
                    queue.append(union_name)

        # Re-sort queue alphabetically for deterministic output
        queue.sort()

    # Check for cycles (shouldn't happen with valid OpenAPI, but let's be safe)
    if len(result) != len(filtered_unions):
        # If there's a cycle, fall back to alphabetical sorting
        return sorted(filtered_unions.values(), key=lambda u: u.name)

    return result


def _generate_ir_version(
    spec: op.OpenAPI,
    *,
    is_private_operation: IsPrivateOperationFilter = _default_is_private_operation,
    get_service_key: ServiceKeyGetter = _default_get_service_key,
    get_method_name: MethodNameGetter = _default_get_method_name,
    normalize_model_name: ModelNameNormalizer = _default_model_name_normalizer,
    normalize_service_name: ServiceNameNormalizer = _default_service_name_normalizer,
) -> APIVersion:
    """
    Generate an intermediate representation of a single OpenAPI spec version.
    """
    services: list[Service] = []

    component_schemas: dict[str, op.Schema] = {}
    if spec.components and spec.components.schemas:
        component_schemas = spec.components.schemas

    inline_schemas: dict[str, op.Schema] = {}
    referenced_names: set[str] = set()
    input_names: set[str] = set()
    output_names: set[str] = set()
    # Map from normalized names to original names for component schemas
    normalized_to_original: dict[str, str] = {}
    for original_name in component_schemas:
        normalized = normalize_model_name(original_name)
        if normalized != original_name:
            normalized_to_original[normalized] = original_name

    if spec.paths is not None:
        for path, path_item in spec.paths.items():
            for http_method, operation in _get_operations(path_item):
                if is_private_operation(operation):
                    continue

                service_key = get_service_key(operation)
                method_name = get_method_name(operation)

                current_service: Service | None = None
                for key in service_key:
                    normalized_key = normalize_service_name(key)
                    if current_service is None:
                        service = next(
                            (s for s in services if s.name == normalized_key), None
                        )
                        if service is None:
                            service = Service(
                                name=normalized_key, services=[], methods=[]
                            )
                            services.append(service)
                        current_service = service
                    else:
                        service = next(
                            (
                                s
                                for s in current_service.services
                                if s.name == normalized_key
                            ),
                            None,
                        )
                        if service is None:
                            service = Service(
                                name=normalized_key, services=[], methods=[]
                            )
                            current_service.services.append(service)
                        current_service = service

                if current_service is None:
                    raise ValueError(
                        f"Operation {operation.operationId} has no service key."
                    )

                resolved_parameters: list[op.Parameter] = [
                    _resolve_reference(p, spec) for p in operation.parameters or []
                ]

                raw_body = _get_body_schema_raw(operation, spec)

                path_params = [
                    _convert_parameter_ir(
                        p,
                        component_schemas,
                        inline_schemas,
                        referenced_names,
                        input_names,
                        normalize_model_name=normalize_model_name,
                        body_schema=raw_body,
                    )
                    for p in resolved_parameters
                    if p.param_in == op.ParameterLocation.PATH
                ]
                query_params = [
                    _convert_parameter_ir(
                        p,
                        component_schemas,
                        inline_schemas,
                        referenced_names,
                        input_names,
                        normalize_model_name=normalize_model_name,
                        body_schema=raw_body,
                    )
                    for p in resolved_parameters
                    if p.param_in == op.ParameterLocation.QUERY
                ]
                response_type, raw_response = _get_success_response_schema_raw(
                    operation, spec
                )
                error_responses_raw = _get_error_responses_raw(operation, spec)

                body = (
                    _convert_typeref(
                        raw_body,
                        component_schemas,
                        inline_schemas,
                        referenced_names,
                        input_names,
                        normalize_model_name=normalize_model_name,
                    )
                    if raw_body is not None
                    else None
                )
                response = (
                    _convert_typeref(
                        raw_response,
                        component_schemas,
                        inline_schemas,
                        referenced_names,
                        output_names,
                        normalize_model_name=normalize_model_name,
                    )
                    if raw_response is not None
                    else None
                )

                errors: list[ErrorResponse] = []
                for (
                    status_code,
                    error_response_type,
                    schema_raw,
                    description,
                ) in error_responses_raw:
                    type = None
                    name = to_pascal_case(f"{method_name}_{status_code}_error")
                    if schema_raw is not None:
                        type = _convert_typeref(
                            schema_raw,
                            component_schemas,
                            inline_schemas,
                            referenced_names,
                            output_names,
                            normalize_model_name=normalize_model_name,
                        )
                    if type is not None and isinstance(type, ModelRef):
                        name = type.name

                    errors.append(
                        ErrorResponse(
                            name=name,
                            status_code=status_code,
                            response_type=error_response_type,
                            type=type,
                            description=description,
                        )
                    )

                pagination: Pagination | None = None
                if operation.__pydantic_extra__ and operation.__pydantic_extra__.get(
                    PAGINATION
                ):
                    pagination_extension = operation.__pydantic_extra__[PAGINATION]
                    pagination_type = pagination_extension["type"]
                    item_schema = _convert_typeref(
                        op.Reference.model_validate(
                            pagination_extension["item_schema"]
                        ),
                        component_schemas,
                        inline_schemas,
                        referenced_names,
                        output_names,
                        normalize_model_name=normalize_model_name,
                    )
                    pagination = Pagination(
                        type=pagination_type,
                        item_schema=item_schema,
                    )

                method = Method(
                    name=method_name,
                    description=operation.description or None,
                    http_method=http_method,
                    path=path,
                    path_params=path_params,
                    query_params=query_params,
                    response_type=response_type,
                    body=body,
                    response=response,
                    errors=errors,
                    deprecated=True if operation.deprecated else None,
                    pagination=pagination,
                )
                current_service.methods.append(method)

    # Iteratively resolve all referenced schemas into named models and enums.
    # Processing a schema may discover new references (transitive deps), so we
    # loop until the referenced set stabilises.
    models_dict: dict[str, Model] = {}
    enums_dict: dict[str, Enum] = {}
    unions_dict: dict[str, NamedUnion] = {}

    processed: set[str] = set()
    while True:
        new_refs = referenced_names - processed
        if not new_refs:
            break
        for name in new_refs:
            processed.add(name)
            is_input = name in input_names
            is_output = name in output_names
            ctx = tuple(
                s
                for s, flag in [(input_names, is_input), (output_names, is_output)]
                if flag
            )

            # Try to find schema by normalized name, or by original name if there's a mapping
            schema = component_schemas.get(name) or inline_schemas.get(name)
            if schema is None:
                original_name = normalized_to_original.get(name)
                if original_name:
                    schema = component_schemas.get(original_name) or inline_schemas.get(
                        original_name
                    )
            if schema is None:
                continue
            # Resolve if schema is a reference to another schema (either Reference object or Schema with $ref)
            # This can be nested, so we need to follow the chain until we get a non-reference schema
            while True:
                ref_value = None
                if isinstance(schema, op.Reference):
                    ref_value = schema.ref
                elif hasattr(schema, "$ref"):
                    ref_value = getattr(schema, "$ref")  # type: ignore[attr-defined]

                if ref_value is None:
                    break

                ref_name = ref_value.split("/")[-1]
                # The reference might point to a different name (normalized vs original)
                resolved_schema = component_schemas.get(ref_name) or inline_schemas.get(
                    ref_name
                )
                if resolved_schema is None:
                    # Try with normalized name
                    resolved_ref_name = normalize_model_name(ref_name)
                    if resolved_ref_name != ref_name:
                        resolved_schema = component_schemas.get(
                            resolved_ref_name
                        ) or inline_schemas.get(resolved_ref_name)
                if resolved_schema is None:
                    break
                schema = resolved_schema
            if schema.enum is not None:
                enums_dict[name] = _schema_to_enum(name, schema)
            elif _is_union_schema(schema):
                unions_dict[name] = _schema_to_named_union(
                    name,
                    schema,
                    component_schemas,
                    inline_schemas,
                    referenced_names,
                    *ctx,
                    normalize_model_name=normalize_model_name,
                )
            else:
                models_dict[name] = _schema_to_model(
                    name,
                    schema,
                    component_schemas,
                    inline_schemas,
                    referenced_names,
                    *ctx,
                    normalize_model_name=normalize_model_name,
                )

    return APIVersion(
        version=spec.info.version,
        services=services,
        input_models=sorted(
            [m for name, m in models_dict.items() if name in input_names],
            key=lambda m: m.name,
        ),
        output_models=sorted(
            [m for name, m in models_dict.items() if name in output_names],
            key=lambda m: m.name,
        ),
        enums=sorted(enums_dict.values(), key=lambda e: e.name),
        input_unions=_topological_sort_unions(unions_dict, input_names),
        output_unions=_topological_sort_unions(unions_dict, output_names),
    )


def generate_ir(
    *specs: op.OpenAPI,
    is_private_operation: IsPrivateOperationFilter = _default_is_private_operation,
    get_service_key: ServiceKeyGetter = _default_get_service_key,
    get_method_name: MethodNameGetter = _default_get_method_name,
    normalize_model_name: ModelNameNormalizer = _default_model_name_normalizer,
    normalize_service_name: ServiceNameNormalizer = _default_service_name_normalizer,
) -> APIIR:
    """
    Generate an intermediate representation of a list of OpenAPI specs.
    """
    versions = [
        _generate_ir_version(
            spec,
            is_private_operation=is_private_operation,
            get_service_key=get_service_key,
            get_method_name=get_method_name,
            normalize_model_name=normalize_model_name,
            normalize_service_name=normalize_service_name,
        )
        for spec in specs
    ]
    return APIIR(versions=versions)
